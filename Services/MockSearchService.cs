        // ฟังก์ชัน Suggestion (Autocomplete)
        public List<string> GetSuggestions(string query)
        {
            if (string.IsNullOrWhiteSpace(query)) return new List<string>();

            return _allResults
                .Where(x => x.Title.Contains(query, StringComparison.OrdinalIgnoreCase))
                .Select(x => x.Title) // เอาแค่ชื่อเรื่อง
                .Take(5) // เอามาแค่ 5 อันพอ เดี๋ยวรก
                .ToList();
        }

        // 🟢 แก้ไข: ฟังก์ชันค้นหา (Search Logic แบบเติมให้เต็ม 12)
        // ฟังก์ชัน Search ที่ล็อกผลลัพธ์ตามบท
        public List<SearchResult> Search(string query)
        {
            if (string.IsNullOrWhiteSpace(query)) return _allResults.Take(12).ToList();

            // Logic พิเศษสำหรับบทละคร (Direct Match)
            // 1. ฉากแรก: พิมพ์ "ข่าวฆาตกรรม" -> ต้องเจอ Timeline อันแรก
            if (query.Contains("ข่าวฆาตกรรม") && !query.Contains("20"))
            {
                var timeline = _allResults.FirstOrDefault(x => x.Id == "timeline-killer-20");
                var others = _allResults.Where(x => x.Id != "timeline-killer-20").Take(11).ToList();
                return new List<SearchResult> { timeline }.Concat(others).ToList();
            }

            // 2. ฉากสอง: พิมพ์ "ข่าวฆาตกรรมสัญลักษณ์ 20" -> ต้องเจอข่าวไทย อันแรก
            if (query.Contains("สัญลักษณ์ 20") || query.Contains("สัญลักษณ์20"))
            {
                var thaiNews = _allResults.FirstOrDefault(x => x.Id == "news-thai-xx");
                var others = _allResults.Where(x => x.Id != "news-thai-xx").Take(11).ToList();
                return new List<SearchResult> { thaiNews }.Concat(others).ToList();
            }

            // 3. ฉากสาม: พิมพ์ "Murder XX 2005 New" (หรือแค่ Murder XX) -> ต้องเจอข่าวอังกฤษ อันแรก
            if (query.Contains("Murder", StringComparison.OrdinalIgnoreCase) || query.Contains("XX", StringComparison.OrdinalIgnoreCase))
            {
                var engNews = _allResults.FirstOrDefault(x => x.Id == "news-english-xx");
                var others = _allResults.Where(x => x.Id != "news-english-xx").Take(11).ToList();
                return new List<SearchResult> { engNews }.Concat(others).ToList();
            }

            // Default Logic (เผื่อพิมพ์คำอื่น)
            var matches = _allResults
                .Where(x => x.Title.Contains(query, StringComparison.OrdinalIgnoreCase) ||
                            x.Snippet.Contains(query, StringComparison.OrdinalIgnoreCase))
                .ToList();

            if (matches.Count >= 12) return matches.Take(12).ToList();

            int missing = 12 - matches.Count;
            var matchIds = matches.Select(x => x.Id).ToHashSet();
            var fillers = _allResults.Where(x => !matchIds.Contains(x.Id)).Take(missing).ToList();

            return matches.Concat(fillers).ToList();
        }

        // ฟังก์ชันดึงข่าวตาม ID (ใช้สำหรับหน้า NewsDetail)
        public SearchResult? GetArticle(string id)
        {
            return _allResults.FirstOrDefault(x => x.Id == id);
        }
